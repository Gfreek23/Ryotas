package com.example.trialproject3.UserDetails;

public class ReadWriteUserDetails {

    public String fullname, Bday, gender, mobile;

    public ReadWriteUserDetails(String userName, String userBday, String userGender, String userPhone){
        this.fullname = userName;
        this.Bday = userBday;
        this.gender = userGender;
        this.mobile = userPhone;
    }

}
